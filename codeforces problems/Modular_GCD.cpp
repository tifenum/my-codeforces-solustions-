ll mod_pow(ll base, ll exp, ll mod) {
    ll result = 1;
    while (exp > 0) {
        if (exp % 2 == 1) {
            result = (result * base) % mod;
        }
        base = (base * base) % mod;
        exp /= 2;
    }
    return result;
}

void solve() {
    ll a, b, n;
    cin >> a >> b >> n;
    a = mod_pow(a, n, MOD);
    b = mod_pow(b, n, MOD);
    ll k = (a + b) % MOD;
    ll c = abs(a - b);
    cout << gcd(c, k) % MOD << endl;
}

